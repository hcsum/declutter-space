export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { lang } = await params;
  if (lang !== "ja") return {};

  return {
    title: "断捨離のやり方——何から捨てる？続けるコツと6ステップ",
    description:
      "断捨離のやり方を具体的に。何から捨てるか、捨てられない物との付き合い方、思い出の品の手放し方、増やさない一進一出まで。判断の軽い物から順に片付ける手順。無料チェックリストPDFつき。",
    keywords: [
      "断捨離 やり方",
      "断捨離 コツ",
      "断捨離 何から",
      "断捨離 できない",
      "断捨離 捨てられない",
      "断捨離 進め方",
    ],
    alternates: buildLanguageAlternates("ja", PATH),
  };
}

export default async function Page({ params }: Props) {
  const { lang } = await params;
  if (lang !== "ja") notFound();

  return (
    <>
      <JsonLd
        data={buildFaqPageSchema(
          copy.faqs.map(([question, answer]) => ({ question, answer })),
        )}
      />
      <JsonLd
        data={buildBreadcrumbSchema("ja", [
          { name: "ホーム", path: "/" },
          { name: "断捨離のやり方", path: PATH },
        ])}
      />
      <SpanishGuide copy={copy} />
    </>
  );
}
